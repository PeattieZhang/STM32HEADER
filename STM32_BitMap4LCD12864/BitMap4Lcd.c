#include "BitMap4Lcd.h"
#include "math.h"

#define PI 3.14159

/* 清空位图 */
void BM_ClearAll(void){
	for(int i = 0; i < BM_ARRAY_LENGTH; i++){
		BM_BITMAP[i] = 0x00;
	}
}

/* 将指定点设为1(即显示时为高电平) */
void BM_SetPoint(uint8_t x, uint8_t y){
	uint8_t x_Block, x_Column;
	
	if(x < BM_WIDTH && y < BM_HEIGHT){
		x_Block = x/8;
		x_Column = x%8;
		BM_BITMAP[y*BM_WIDTH/8+x_Block] |= (0x01<<(7-x_Column));
	}
}

/* 将指定点设为0(即显示时为低电平) */
void BM_ClearPoint(uint8_t x, uint8_t y){
	uint8_t x_Block, x_Column;
	
	if(x < BM_WIDTH && y < BM_HEIGHT){
		x_Block = x/8;
		x_Column = x%8;
		BM_BITMAP[y*BM_WIDTH/8+x_Block] &= (~(0x01<<(7-x_Column)));
	}
}

/* 作sin图，用来做练习的，没啥实际用处 */
void BM_Sine(uint8_t X, uint8_t Y, uint8_t Width, uint8_t Height, uint8_t Period_Num){
	uint8_t Amp = Height/2-1;											//幅值
	uint8_t x, y, y_tmp;
	uint8_t i, j;
	
	y_tmp = Y+Amp+1;
	BM_ClearAll();
	for(i = 0; i < Width; i++){
		//以下为取每个横坐标下对应的sin点
		x = i+X;
		y = (uint8_t)(Y+Amp+1-Amp*sin(2*Period_Num*i*PI/(Width-1))+0.5);
		BM_SetPoint(x, y);
		
		//以下为保证图形连续的操作
		if(y>y_tmp+1){															//图像下行时	
			for(j = y_tmp+1; j<(y_tmp+y)/2; j++){
				BM_SetPoint(x-1, j);
			}
			for(j = (y_tmp+y)/2; j<y; j++){
				BM_SetPoint(x, j);
			}
		}
		else if(y+1 < y_tmp){												//图像上行时
			for(j = y+1; j<(y_tmp+y)/2; j++){
				BM_SetPoint(x, j);
			}
			for(j = (y_tmp+y)/2; j<y_tmp; j++){
				BM_SetPoint(x-1, j);
			}
		}
		
		y_tmp = y;
	}
}

